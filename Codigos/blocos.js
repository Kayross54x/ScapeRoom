var SpriteBlocos = function(posX, posY, width, height, color){ //Criando a classe sprite que vai receber esses parâmentros (necessários para criação do sprite)
    this.posX = posX;
    this.posY = posY;
    this.width = width; //Função construtora do js
    this.height = height;
    this.color = color;
    this.visible = true;
    
}